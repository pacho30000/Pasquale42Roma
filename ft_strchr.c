#include "libft.h"

char    *ft_strchr(const char *s, int c)
{
    int i;
    
    i = 0;
    while (s[i] != '\0')
    {
        if (s[i] != '\0')
        {
            while (s[i] != c)
                i++;
            while (s[i] != '\0')
            {
                write(1, &s[i], 1);
                i++;
            }
        }
    }
    return (0);
}
