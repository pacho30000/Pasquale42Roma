#include "libft.h"

char    *ft_strrchr(const char *s, int c)
{
    int i;

    i = 0;
    while (s[i] != '\0')
        i++;
    while (i == 0)
    {
        if (s[i] != c)
            i--;
        else
        {
            while (i == 0)
            {
                write (1, &s[i], 1);
                i--;
            }
        }
    }
    return (0);
}
